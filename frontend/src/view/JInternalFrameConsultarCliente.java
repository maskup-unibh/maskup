    private void limparTabela() {
        modelo = (DefaultTableModel) jTableCOnsultarCliente.getModel();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
    }

    private void lerLinhaTabela(Cliente c) {

        int linha = this.jTableCOnsultarCliente.getSelectedRow();
        c.setCodigo(Integer.parseInt(jTableCOnsultarCliente.getValueAt(linha, 0).toString()));
        c.setNome(jTableCOnsultarCliente.getValueAt(linha, 1).toString());
        c.setCpf(jTableCOnsultarCliente.getValueAt(linha, 2).toString());
        c.setRg(jTableCOnsultarCliente.getValueAt(linha, 3).toString());
        c.setTelefone(jTableCOnsultarCliente.getValueAt(linha, 4).toString());
        c.setEmail(jTableCOnsultarCliente.getValueAt(linha, 5).toString());
    }

    private void preencheTabela(ArrayList<Cliente> listC) {

        modelo = (DefaultTableModel) jTableCOnsultarCliente.getModel();

        String[] linha = {null, null, null, null, null, null};
        for (int i = 0; i < listC.size(); i++) {
            modelo.insertRow(i, linha);
            modelo.setValueAt(listC.get(i).getCodigo(), i, 0);
            modelo.setValueAt(listC.get(i).getNome(), i, 1);
            modelo.setValueAt(listC.get(i).getCpf(), i, 2);
            modelo.setValueAt(listC.get(i).getRg(), i, 3);
            modelo.setValueAt(listC.get(i).getTelefone(), i, 4);
            modelo.setValueAt(listC.get(i).getEmail(), i, 5);

        }
    }


