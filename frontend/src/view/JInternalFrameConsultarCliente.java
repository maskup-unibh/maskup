    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        Cliente c = new Cliente();
        lerLinhaTabela(c);
        JInternalFrameEditarCliente jiec = new JInternalFrameEditarCliente(tela, c);
        tela.jdp.add(jiec);
        jiec.setVisible(true);
        jButtonPesquisar.setEnabled(true);
        jButtonAlterar.setEnabled(false);
        jButtonExcluir.setEnabled(false);
        limparTabela();

    }//GEN-LAST:event_jButtonAlterarActionPerformed

    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        ArrayList<Cliente> listC = new ArrayList();
        ClienteDAO cd = new ClienteDAO();
        listC = cd.readAll();
        preencheTabela(listC);
        jButtonAlterar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        ClienteDAO cd = new ClienteDAO();
        int linha = this.jTableCOnsultarCliente.getSelectedRow();
        int id = Integer.parseInt(jTableCOnsultarCliente.getValueAt(linha, 0).toString());
        cd.removeByID(id);
        JOptionPane.showMessageDialog(rootPane, "Cliente excluÃ­do com sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        limparTabela();
        jButtonPesquisar.setEnabled(true);
    }//GEN-LAST:event_jButtonExcluirActionPerformed

